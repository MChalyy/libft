char	*ft_strrchr(char *str, char a)
{
	int	c;
	int	p;
	int	con;

	con = 0;
	p = 0;
	c = 0;
	while (str[c] != '\0')
	{
		if (str[c] == a)
		{
			p = c;
			con = 1;
		}
		c++;
	}
	if (con == 1)
		return (&str[p]);
	else
		return (0);
}
